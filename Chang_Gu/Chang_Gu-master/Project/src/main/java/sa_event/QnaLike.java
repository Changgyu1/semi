package sa_event;

public class QnaLike {
	private int qna_number;
	private String name;
	private int qnaCount;
	private int qnaunlike;


	public int getQna_number() {
		return qna_number;
	}
	public void setQna_number(int qna_number) {
		this.qna_number = qna_number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getQnaCount() {
		return qnaCount;
	}
	public void setQnaCount(int qnaCount) {
		this.qnaCount = qnaCount;
	}
	public int getQnaunlike() {
		return qnaunlike;
	}
	public void setQnaunlike(int qnaunlike) {
		this.qnaunlike = qnaunlike;
	}
	
}
